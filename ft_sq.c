#include "mathft.h"

int	ft_sq(int  pot)
{
	int	i;
	int	num;

	num = 2;
	if (pot == 0)
		return (1);
	if (pot < 0)
		return (NAN);
	i = 1;
	while (i++ < pot)
		num *= 2;
	return (num);
}
